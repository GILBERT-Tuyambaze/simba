type I18nContextValue = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: string, fallback?: string) => string;
  translateCategory: (name: string) => string;
  suggestedLanguage: LanguageCode | null;
  dismissSuggestedLanguage: () => void;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function getStoredLanguage(): LanguageCode {
  try {
    const stored = localStorage.getItem(LANGUAGE_KEY);
    if (stored === 'rw' || stored === 'fr' || stored === 'en') {
      return stored;
    }
  } catch {
    // Ignore storage failures.
  }

  const browserLanguages = typeof navigator !== 'undefined' ? navigator.languages || [navigator.language] : [];
  const joined = browserLanguages.join(' ').toLowerCase();
  if (joined.includes('rw')) {
    return 'rw';
  }
  if (joined.includes('fr')) {
    return 'fr';
  }

  try {
    const timezone = Intl.DateTimeFormat().resolvedOptions().timeZone;
    if (timezone === 'Africa/Kigali') {
      return 'rw';
    }
  } catch {
    // Ignore locale detection failures.
  }

  return 'en';
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<LanguageCode>(getStoredLanguage);
  const [suggestedLanguage, setSuggestedLanguage] = useState<LanguageCode | null>(null);

  useEffect(() => {
    try {
      localStorage.setItem(LANGUAGE_KEY, language);
    } catch {
      // Ignore storage failures.
    }
  }, [language]);

  useEffect(() => {
    try {
      if (localStorage.getItem('simba_language_suggestion_dismissed') === '1') {
        return;
      }
    } catch {
      // Ignore storage failures.
    }

    const browserLanguages =
      typeof navigator !== 'undefined' ? navigator.languages || [navigator.language] : [];
    const joined = browserLanguages.join(' ').toLowerCase();
    const timezone =
      typeof Intl !== 'undefined'
        ? Intl.DateTimeFormat().resolvedOptions().timeZone
        : '';

    let nextSuggestion: LanguageCode | null = null;
    if (joined.includes('rw')) {
      nextSuggestion = 'rw';
    } else if (joined.includes('fr')) {
      nextSuggestion = 'fr';
    } else if (timezone === 'Africa/Kigali' && language === 'en') {
      nextSuggestion = 'rw';
    }

    if (nextSuggestion && nextSuggestion !== language) {
      setSuggestedLanguage(nextSuggestion);
    }
  }, [language]);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key: string, fallback?: string) => {
        const value = DICTIONARY[language][key];
        if (typeof value === 'string') {
          return value;
        }
        return fallback || key;
      },
      translateCategory: (name: string) =>
        CATEGORY_LABELS[name]?.[language] || CATEGORY_LABELS[name]?.en || name,
      suggestedLanguage,
      dismissSuggestedLanguage: () => {
        setSuggestedLanguage(null);
        try {
          localStorage.setItem('simba_language_suggestion_dismissed', '1');
        } catch {
          // Ignore storage failures.
        }
      },
    }),
    [language, suggestedLanguage]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider');
  }
  return context;
}
